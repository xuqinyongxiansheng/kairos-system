_MODULE_CACHE = {}


def __getattr__(name):
    """延迟导入：只在访问时才加载对应模块"""
    if name in _LAZY_MODULES:
        module_path = _LAZY_MODULES[name]
        if module_path not in _MODULE_CACHE:
            _MODULE_CACHE[module_path] = importlib.import_module(module_path, __name__)
        module = _MODULE_CACHE[module_path]
        if hasattr(module, name):
            return getattr(module, name)
    raise AttributeError(f"module {__name__!r} has no attribute {name!r}")


def __dir__():
    return list(_LAZY_MODULES.keys())


__all__ = list(_LAZY_MODULES.keys())
